# logical operator
print(True and False)
print(False and False) 
print(True or True)
print(True or False)
print(False or False)
print(not  True)
print(not False)
print(2 is 2.0)
print(2 is not 2.0)

# bitwise operators
print(3&6) #----> & ---> and 
print(3|6) #----> | --->or
print(3^6) #----> ^ --->xor

